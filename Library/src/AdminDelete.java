    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        conn = Session.getConn();//DBConnect.DBConnect();
        for(Book book: allBooks){
            try {
                if (book.toString().equals(jList1.getSelectedValue())) {
                    int BookID = book.getId();
                    String query = "DELETE FROM Books " +
                                   "WHERE id = ?";
                    PreparedStatement preparedStmt = conn.prepareStatement(query);
                    preparedStmt.setInt(1, BookID);
                    preparedStmt.executeUpdate();
                    JOptionPane.showMessageDialog(null, "Deleted Book " + book.getTitle());
                    listBooks.clear();
                    jList1.setModel(listBooks);
                    jTextField1.setText("");
                    bg1.clearSelection();
                }
            } catch (SQLException e) {
                System.out.println(e);
            }
        }
    }//GEN-LAST:event_jButton4ActionPerformed

